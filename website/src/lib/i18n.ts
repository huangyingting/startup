export type StringKey = keyof typeof strings.en;

export function t(key: StringKey, locale: Locale = 'en'): string {
  const bundle = strings[locale] ?? strings.en;
  return (bundle as Record<string, string>)[key] ?? strings.en[key];
}

export function tFormat(key: StringKey, vars: Record<string, string | number>, locale: Locale = 'en'): string {
  let template = t(key, locale);
  for (const [name, value] of Object.entries(vars)) {
    template = template.replace(new RegExp('\\{' + name + '\\}', 'g'), String(value));
  }
  return template;
}
